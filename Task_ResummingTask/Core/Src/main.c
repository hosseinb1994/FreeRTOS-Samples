typedef uint32_t TaskProfiler;

TaskProfiler First_Task, Second_Task, Thrid_Task, Fourt_Task;
TaskHandle_t First_handle, Second_handle, Third_handle, Fourt_handle;

TaskProfiler Third_Task_Priority;
uint32_t suspend_monitor;
uint32_t resume_monitor;
bool is_suspended = false;
void vControllerTask1(void *pvParameters);
void vControllerTask2(void *pvParameters);
void vControllerTask3(void *pvParameters);
void vControllerTask4(void *pvParameters);

int main(void)
{

  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_USART2_UART_Init();

  xTaskCreate(vControllerTask1,
		  "First Task1 Controller",
		  100,
		  NULL,
		  1,
		  &First_handle);
  xTaskCreate(vControllerTask2,
 		  "Second Task2 Controller",
 		  100,
		  NULL,
 		  1,
		  &Second_handle);

  xTaskCreate(vControllerTask3,
   		  "Third Task3 Controller",
   		  100,
		  NULL,
   		  1,
		  &Third_handle);
  xTaskCreate(vControllerTask4,
		  "Fourth Task4 Controller",
		  100,
		  NULL,
		  1,
		  &Fourt_handle);

  vTaskStartScheduler();
  while (1)
  {
  }
}

//Execute all four tasks using a single task function.
void vControllerTask1(void *pvParameters)
{
	while(1){
		First_Task++;
		HAL_Delay(100);
	}

}
void vControllerTask2(void *pvParameters)
{
	while(1){
		Second_Task++;
		HAL_Delay(100);
		suspend_monitor++;
		if(suspend_monitor >= 50){
			is_suspended = true;
			suspend_monitor = 0;
			vTaskSuspend(NULL);
		}

	}

}

void vControllerTask3(void *pvParameters)
{
	while(1){
		Thrid_Task++;
		HAL_Delay(100);
		/*
		 * uxTaskPriorityGet returns task priority.
		 * */
		Third_Task_Priority = uxTaskPriorityGet(Third_handle);
		if(is_suspended){
			resume_monitor++;
			if(resume_monitor >=30){
				vTaskResume(Second_handle);
				resume_monitor = 0;
				is_suspended = false;
			}
		}
	}

}

void vControllerTask4(void *pvParameters)
{
	while(1){
		Fourt_Task++;
	}

}



